#Integers in python
num=10
print("num is the type of:",type(num))

#List in python
lst=[10,20,30,40]
print("lst is type of:",type(lst))

#Float in python
decimalNum=10.04
print("decimalNum is type of:",type(decimalNum))

#set in python
setVar={10,20,30,40}
print("setVar in python is:",type(setVar))

#string in python
stringVar="I love python"
print("stringVar is type of:",type(stringVar))

#Dictionary in python
dictionary={
    "name":"Omkar",
    "age":21
}
print("dictionary is type of:",type(dictionary))

#complex number in python
compNum=3.14j
print("compNum is type of:",type(compNum))

#boolean
boolVar=True
print("boolVar is type of:",type(boolVar))

#tuple
tupVar=(10,20,30,40)
print("tupVar is type of:",type(tupVar))